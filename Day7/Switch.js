var a = "manish";
switch (a) {
  case "sachin":
    console.log("A is Sachin");
    break;
  case "manish":
    console.log("A is manish");
    break;
  case "sapna":
    console.log("A is Sapna");
    break;
  default:
    console.log("Name not matched");
    break;
}
console.log("Switch case Ended");